package sheridan;

public class Sphere
{
    public static void main(String[] args) 
    {
        //Declarations
        double radius = 2.2; // represents radius
        double pi = 3.1415; // represents pi
        
        //Calculate the volume and assing it to volume
        double volume = 4.0 / 3 * pi * radius * radius * radius;
        
        //Output volume
        System.out.println ("The volume is: " + volume);
        
    }
}